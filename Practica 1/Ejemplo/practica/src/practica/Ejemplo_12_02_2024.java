        /*
        Explicación:
        La operación 10 % 2 se calcula así:
        Se divide 10 / 2, lo que da 5 (división entera).
        Se multiplica 5 * 2 = 10.
        Se resta 10 - 10 = 0 (este es el residuo).
        Por lo tanto, 10 % 2 devuelve 0.
        */
        
